    public InterfazAdministrarProducto() {
        initComponents();
        
        int i=0;
        int j=0;
        
        
        try {
                
                conexion=con.establecerConexion();
                PreparedStatement pst = conexion.prepareStatement("SELECT * FROM PRODUCTO ORDER BY marcaProducto ASC");
                ResultSet rs = pst.executeQuery();
                
                while(rs.next()){
                    jLabelFoto=(JLabel) jPanel1.getComponent(8+i);
                    jLabelMarca=(JLabel) jPanel1.getComponent(9+i);
                    jLabelModelo=(JLabel) jPanel1.getComponent(10+i);
                    i=i+3;
                    codProdutoLista[j]=rs.getInt("codProducto");
                    j++;
                    jLabelMarca.setText(rs.getString("marcaProducto"));
                    jLabelModelo.setText(rs.getString("modeloProducto"));
                    
                    Blob blob = rs.getBlob("fotoProducto");
                    //pasar el binario a imagen
                    byte[] data = blob.getBytes(1, (int) blob.length());
                    //lee la imagen
                    BufferedImage img = null;
                    try {
                        img = ImageIO.read(new ByteArrayInputStream(data));
                    } catch (IOException e) {
                        Logger.getLogger(InterfazEliminarProducto.class.getName()).log(Level.SEVERE, null, e);
                    }                    
                    ImageIcon icono = new ImageIcon(img);
                    Icon imagen = new ImageIcon(icono.getImage().getScaledInstance(jLabelFoto.getWidth(), jLabelFoto.getHeight(), Image.SCALE_DEFAULT));
                    jLabelFoto.setIcon(imagen);
                    
                }
                
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Â¡Error al cargar!");
                System.out.println("Error al cargar foto: " + e);
            }
    }

